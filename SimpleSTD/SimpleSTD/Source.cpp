#include "iostream"
// int - ціле число, яке повертає функція main
// Це потрібно, щоб при завершені роботи программи, ОС - знав, як вона завершила роботу
int main()
{
	using namespace std; // Це для того, щоб не писати слово std::
	cout << "What's up, Doc!\n"; // \n - перехід на новий рядок
	return 0; // int main() - тому пишу return 0;, щоб сказати ОС, що програма завершилася успішно
}